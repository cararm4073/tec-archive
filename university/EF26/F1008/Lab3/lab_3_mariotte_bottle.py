import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

"""#Measurements

##Diameters
"""

bottle = 6.0
hole = 0.4

"""##Areas"""

areaBottle = np.pi * (bottle/2)**2
areaHole = np.pi * (hole/2)**2
areas = areaBottle / areaHole

"""#Load files"""

v1 = 'https://raw.githubusercontent.com/cararm4073/tec-archive/refs/heads/main/university/EF26/F1008/Lab3/video1.csv'
v2 = 'https://raw.githubusercontent.com/cararm4073/tec-archive/refs/heads/main/university/EF26/F1008/Lab3/video2.csv'
v3 = 'https://raw.githubusercontent.com/cararm4073/tec-archive/refs/heads/main/university/EF26/F1008/Lab3/video3.csv'
v4 = 'https://raw.githubusercontent.com/cararm4073/tec-archive/refs/heads/main/university/EF26/F1008/Lab3/video4.csv'
v5 = 'https://raw.githubusercontent.com/cararm4073/tec-archive/refs/heads/main/university/EF26/F1008/Lab3/video5.csv'

"""##Data frames"""

df1 = pd.read_csv(v1)
df2 = pd.read_csv(v2, skiprows=1)
df3 = pd.read_csv(v3, skiprows=1)
df4 = pd.read_csv(v4)
df5 = pd.read_csv(v5, skiprows=1)

"""##Clear empty data and set variables"""

df1 = df1.dropna(subset=['t', 'y'])
t1 = df1['t'].values
y1 = df1['y'].values

df2 = df2.dropna(subset=['t', 'y'])
t2 = df2['t'].values
y2 = df2['y'].values

df3 = df3.dropna(subset=['t', 'y'])
t3 = df3['t'].values
y3 = df3['y'].values

df4 = df4.dropna(subset=['t', 'y'])
t4 = df4['t'].values
y4 = df4['y'].values

df5 = df5.dropna(subset=['t', 'y'])
t5 = df5['t'].values
y5 = df5['y'].values

"""#Maths

##Gradients
The actual derivatives for height or position y. Using the - sign as it decreases

Derivative of y position respect to time
"""

vLevel1 = -np.gradient(y1, t1)
vLevel2 = -np.gradient(y2, t2)
vLevel3 = -np.gradient(y3, t3)
vLevel4 = -np.gradient(y4, t4)
vLevel5 = -np.gradient(y5, t5)

"""##Output velocities
Or the efflux velocities
"""

vOut1 = vLevel1 * areas
vOut2 = vLevel2 * areas
vOut3 = vLevel3 * areas
vOut4 = vLevel4 * areas
vOut5 = vLevel5 * areas

"""-----
#Plots

##Overall plot
General plot with the 5 repetitions
"""

