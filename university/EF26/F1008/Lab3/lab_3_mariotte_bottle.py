plt.figure(figsize=(14, 5))
plt.plot(t1, vLevel1, 'b-', label='Video 1')
plt.plot(t2, vLevel2, 'g-', label='Video 2')
plt.plot(t3, vLevel3, 'r-', label='Video 3')
plt.plot(t4, vLevel4, 'm-', label='Video 4')
plt.plot(t5, vLevel5, 'y-', label='Video 5')

plt.title('Water level velocity vs Time')
plt.xlabel('Time (s)')
plt.ylabel('Water level velocity (cm/s)')
plt.grid(True)
plt.legend()
plt.show()

# @title Output velocity vs time
plt.figure(figsize=(14, 5))
plt.plot(t1, vOut1, 'b-', label='Video 1')
