"""___

# 🔺 Stage 1: Free fall

## Now the code...

###Differential equation definition
"""

# @title
#function that will use the scipy.integrate library later on. using vectors of both the current time (t) and a "state vector" that will have the height and speed at the given time
def dif_eq(t,y):
  #array structure
  position = y[0] #the height or position
  speed = y[1]    #the speed

  # differential equation for free fall
  # there is nothing extra, so its just gravity constant
  accel = gravity

  #return the derivatives dy/dt and dv/dt, corresponding to speed and acceleration at given time
  return [speed, accel]

"""###Define when the gondola reaches the ground"""


def hit_ground(t, y):
  return y[0] - height_braking  

hit_ground.terminal = True
hit_ground.direction = -1


"""###Solve the equation (simulation)"""

solution1 = solve_ivp(dif_eq, [0, t_max], [y_i, v_i], events=hit_ground, dense_output=True, max_step=0.05 )

