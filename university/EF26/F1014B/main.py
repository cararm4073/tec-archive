"""##Now the code...

###Magnetic braking equation
Using Eddy currents formula
"""

def magnetic_braking(t, y):
    speed = y[1]
    accel = gravity - (k / mass) * speed

    return [speed, accel]

"""###Define when the gondola reaches the ground"""

def hit_ground2(t, y):
    return y[0] - 0.0

hit_ground2.terminal = True
hit_ground2.direction = -1

"""###Solve the equation"""

solution2 = solve_ivp(magnetic_braking, [t_i2, t_max], [y_i2, v_i2], events=hit_ground2, dense_output=True, max_step=0.05)

"""##🗒 Showing the results"""

print(f"Final velocity when the gondola reaches the ground: {solution2.y[1,-1]:.3f} m/s")

"""___
#🔺 Stage 3: Merge the data

## Concatenate arrays
"""

total_time = np.concatenate((solution1.t, solution2.t))
