"""# 3. Linear regression (age and miles)
Fit a multiple linear regression model to estimate the selling price based on age and mileage
"""

# @title
X1 = df[['Age', 'Miles']]
X1 = sm.add_constant(X1) #add constant
y = df['Price']

model1 = sm.OLS(y, X1).fit()
print("\n--- Regression model (Age and miles) ---\n\n")
print(model1.summary())

"""# 4. Linear regression (all variables)
Fit a multiple linear regression model considering all provided variables
"""

# @title
X2 = df[['Age', 'Miles', 'Condition', 'Excellent', 'Owner']]
X2 = sm.add_constant(X2)
y  = df['Price']

model2 = sm.OLS(y, X2).fit()
print("\n--- Regression model (All variables) ---\n\n")
print(model2.summary())