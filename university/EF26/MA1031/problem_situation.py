import pandas as pd
import statsmodels.api as sm
import matplotlib.pyplot as plt
import seaborn as sns

"""# Load file and data frame"""

df = pd.read_csv("https://raw.githubusercontent.com/cararm4073/tec-archive/refs/heads/main/university/EF26/MA1031/Basededatos_SituacionProblemaE1.csv")
df.columns = df.columns.str.strip() #clean the columns

#remove the $ sign from the column so that we can use numbers and not text vars
df['Price'] = (df['Price']
                            .astype(str)
                            .str.replace('$', '', regex=False)
                            .str.replace(',', '', regex=False)
                            .str.strip()
                            .astype(float))

"""# 1. Scatter plot (age and price)"""

# @title
plt.figure(figsize=(8, 5))
sns.scatterplot(data=df, x='Age', y='Price')
plt.title('Scatter plot: Age vs Sale price')
plt.xlabel('Age(years)')
plt.ylabel('Sale price (pesos)')
plt.savefig('age_vs_price.png')
plt.plot()

